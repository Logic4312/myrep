// Counter animation for package prices
function animateCounter(element, target, duration = 2000) {
    let start = 0;
    const increment = target / (duration / 16);
    
    function updateCounter() {
        start += increment;
        if (start < target) {
            element.textContent = Math.floor(start).toLocaleString();
            requestAnimationFrame(updateCounter);
        } else {
            element.textContent = target.toLocaleString();
        }
    }
    
    updateCounter();
}

// Intersection Observer for counter animation
const counterObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            const priceElement = entry.target.querySelector('.price');
            if (priceElement) {
                const priceText = priceElement.textContent;
                const priceNumber = parseInt(priceText.replace(/[^\d]/g, ''));
                if (priceNumber) {
                    animateCounter(priceElement, priceNumber);
                }
            }
            counterObserver.unobserve(entry.target);
        }
    });
}, { threshold: 0.5 });

// Observe package cards for counter animation
document.querySelectorAll('.package-card').forEach(card => {
    counterObserver.observe(card);
});

